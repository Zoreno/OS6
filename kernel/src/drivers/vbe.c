// Interface functions
//=============================================================================

void vbe_bochs_set_gfx(uint16_t width, uint16_t height, uint16_t bpp)
{
    vbe_bochs_write(VBE_DISPI_INDEX_ENABLE, VBE_DISPI_DISABLED);
    vbe_bochs_write(VBE_DISPI_INDEX_XRES, width);
    vbe_bochs_write(VBE_DISPI_INDEX_YRES, height);
    vbe_bochs_write(VBE_DISPI_INDEX_BPP, bpp * 8);
    vbe_bochs_write(VBE_DISPI_INDEX_ENABLE, VBE_DISPI_ENABLED | VBE_DISPI_LFB_ENABLED);

    _vbe_current_context.width = width;
    _vbe_current_context.height = height;
    _vbe_current_context.bpp = bpp;
    _vbe_current_context.framebuffer_p = (uint64_t)pci_get_vga_lfb();
    _vbe_current_context.framebuffer_v = (uint64_t)0xE0000000 | _vbe_current_context.framebuffer_p & 0xFFF;

    // TODO_ Get page size from virtmem
    size_t buf_pages = align_up(width * height * bpp, 4096) / 4096;

    virt_mem_map_pages(_vbe_current_context.framebuffer_p,
                       _vbe_current_context.framebuffer_v,
                       buf_pages,
                       0);
}

void vbe_clear_screen(vbe_pixel_t pixel)
{
    union {
        vbe_pixel_t p;
        uint32_t i;
    } pi;

    pi.p = pixel;

    vbe_pixel_t *lfb = vbe_get_buffer();

    memsetl(lfb, pi.i, _vbe_current_context.width * _vbe_current_context.height);
}

void vbe_fill_rect(vbe_pixel_t pixel, int32_t x, int32_t y, uint32_t width, uint32_t height)
{
    union {
        vbe_pixel_t p;
        uint32_t i;
    } pi;

    pi.p = pixel;

    vbe_pixel_t *lfb = vbe_get_buffer();

    int32_t max_x = x + width;
    int32_t max_y = y + height;

    if (x < 0)
    {
        x = 0;
    }

    if (y < 0)
    {
        y = 0;
    }

    if (max_x > _vbe_current_context.width)
    {
        max_x = _vbe_current_context.width;
    }

    if (max_y > _vbe_current_context.height)
    {
        max_y = _vbe_current_context.height;
    }

    uint32_t stride = _vbe_current_context.width;

    for (; y < max_y; ++y)
    {
        memsetl(lfb + y * stride + x, pi.i, max_x - x);
    }
}

void vbe_put_pixel(vbe_pixel_t pixel, int32_t x, int32_t y)
{
    vbe_pixel_t *lfb = vbe_get_buffer();

    if (x < 0 || y < 0)
    {
        return;
    }

    if (x > _vbe_current_context.width || y > _vbe_current_context.height)
    {
        return;
    }

    lfb[x + _vbe_current_context.width * y] = pixel;
}

