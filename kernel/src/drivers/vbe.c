void vbe_put_char(vbe_pixel_t pixel, int32_t x, int32_t y, char c)
{
    uint8_t *character = font8x8_basic[c];

    for (uint32_t cy = 0; cy < char_height; ++cy)
    {
        for (uint32_t cx = 0; cx < char_width; ++cx)
        {
            if (*(character + (char_width - cy - 1)) & (1 << cx))
            {
                vbe_put_pixel(pixel, x + cx, y - cy);
            }
        }
    }
}

void vbe_print_string(vbe_pixel_t pixel, int32_t x, int32_t y, char *str)
{
    int32_t xpos = x;
    int32_t ypos = y;

    while (*str)
    {
        switch (*str)
        {
        case '\n':
            xpos = x;
            ypos += char_height;
            break;
        default:
            vbe_put_char(pixel, xpos, ypos, *str);
            xpos += char_width;
            break;
        }

        ++str;
    }
}

