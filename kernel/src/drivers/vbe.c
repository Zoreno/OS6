typedef struct _vbe_rendering_context_t
{
    uint16_t width;
    uint16_t height;
    uint16_t bpp;
    uint64_t framebuffer_p;
    uint64_t framebuffer_v;
} vbe_rendering_context_t;

//=============================================================================
// Private variables
//=============================================================================

static vbe_rendering_context_t _vbe_current_context = {0};

//=============================================================================
// Private functions forwards
//=============================================================================

static void vbe_bochs_write(uint16_t index, uint16_t value);
static uint64_t align_up(uint64_t val, uint64_t align);

//=============================================================================
// Private functions
//=============================================================================

static void vbe_bochs_write(uint16_t index, uint16_t value)
{
    outportw(VBE_DISPI_IOPORT_INDEX, index);
    outportw(VBE_DISPI_IOPORT_DATA, value);
}

static uint64_t align_up(uint64_t val, uint64_t align)
{
    if (!align)
    {
        return val;
    }

    --align;

    return (val + align) & ~align;
}

